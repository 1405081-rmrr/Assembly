.MODEL small

.STACK 100h

.DATA

MSG1 DB 'ENTER A UPPER CASE CHARACTER: $' 
MSG2 DB 0DH,0AH,'IN LOWER CASE ITS PREVIOUS CHARACTER IS : $'  
MSG3 DB 0DH,0AH,'ITS 1S COMPLIMENTIS  :$'
CHAR DB ?, '$' 
C DB ?,'$'

.CODE

MAIN PROC

mov AX,@DATA
mov DS,AX

; PROMPT
lea DX,MSG1
mov AH,9
int 21H

;INPUT A CHARACTER AND CONVERT TO UPPER CASE
mov AH,1
int 21H 
MOV CL,AL
ADD AL,1FH
mov CHAR, AL

;DISPLAY ON THE NEXT LINE
lea DX,MSG2
mov AH,9
int 21H  

MOV AH,2
MOV DL,CHAR
INT 21H


MOV BL,11111111B
SUB BL,CL
MOV C,BL 

lea DX,MSG3
MOV AH,9
INT 21H

mov AH,2
mov DL,C
int 21h
        


                
;TERMINATION         
mov AH,4CH
int 21H

MAIN ENDP
END MAIN
